package day22_interview;

public class Q12 {
    public static void main(String[] args) {
        /*
        Write a Java program that prints ascii values of characters from ‘a’ to ‘z’

         */
        int sayi=0;
        for (int i = 'a'; i <'z' ; i++) {
            sayi=i;
            System.out.print(sayi+" ");
        }
    }
}
